LOG.debug = loglevel.debug; // 1
LOG.info = loglevel.info; // 2
LOG.warn = loglevel.warn; // 3
LOG.error = loglevel.error; // 4
// 5 = silent


//////////////////////
///// ES6 EXPORT /////
//////////////////////

export { LOG };


